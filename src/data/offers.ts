import { buildOfferSlug, type OfferPlan, type OfferType } from '../lib/offer-slug'

export type SeedOffer = {
  type: OfferType
  plan: OfferPlan
  pricePerYear: number
  features: string[]
  slug: string
}

function offer(type: OfferType, plan: OfferPlan, pricePerYear: number, features: string[]): SeedOffer {
  return {
    type,
    plan,
    pricePerYear,
    features,
    slug: buildOfferSlug(type, plan),
  }
}

